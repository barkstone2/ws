package score;

public class ScoreDTO {
	
	private int no;
	private String hakyun;
	private String testType;
	private int kor;
	private int eng;
	private int mat;
	private int sci;
	private int tot;
	private double avg;
	private String sid;

	
	public ScoreDTO() {
	}


	public int getNo() {
		return no;
	}


	public void setNo(int no) {
		this.no = no;
	}


	public String getHakyun() {
		return hakyun;
	}


	public void setHakyun(String hakyun) {
		this.hakyun = hakyun;
	}


	public String getTestType() {
		return testType;
	}


	public void setTestType(String testType) {
		this.testType = testType;
	}


	public int getKor() {
		return kor;
	}


	public void setKor(int kor) {
		this.kor = kor;
	}


	public int getEng() {
		return eng;
	}


	public void setEng(int eng) {
		this.eng = eng;
	}


	public int getMat() {
		return mat;
	}


	public void setMat(int mat) {
		this.mat = mat;
	}


	public int getSci() {
		return sci;
	}


	public void setSci(int sci) {
		this.sci = sci;
	}


	public int getTot() {
		return tot;
	}


	public void setTot(int tot) {
		this.tot = tot;
	}


	public double getAvg() {
		return avg;
	}


	public void setAvg(double avg) {
		this.avg = avg;
	}


	public String getSid() {
		return sid;
	}


	public void setSid(String sid) {
		this.sid = sid;
	}
	
	
	
	
}
