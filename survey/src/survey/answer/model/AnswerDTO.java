package survey.answer.model;

public class AnswerDTO {
	private int no;
	private int servey_no;
	private int servey_answer;
	
	public AnswerDTO() {
	}

	public int getNo() {
		return no;
	}

	public void setNo(int no) {
		this.no = no;
	}

	public int getServey_no() {
		return servey_no;
	}

	public void setServey_no(int servey_no) {
		this.servey_no = servey_no;
	}

	public int getServey_answer() {
		return servey_answer;
	}

	public void setServey_answer(int servey_answer) {
		this.servey_answer = servey_answer;
	}
	
	
	
}
